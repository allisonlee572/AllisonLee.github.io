function generateSlug(title) {
  //.replace(/\s+/g, "-")
  // \s matches 1+ mspaces
  // g is global - match all spaces - not just the first
  // replace with -

  // [^a-z0-9\-]
  // replace any non-URL-safe characters
  // ^ means NOT
  // so this means: anything that is not a-z, 0-9, or a -

  return title
    .toLowerCase()
    .trim()
    .replace(/\s+/g, "-")
    .replace(/[^a-z0-9\-]/g, "");
}

